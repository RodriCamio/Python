# Conversion de tipos de datos

# Definimos una variable de tipo string

numero_str = "12345"

total = int(numero_str) + 100
print(f"El total es: {total}") # Imprime: El total es: 12445

concatenacion = numero_str + str(67890)
print(f"Concatenación de cadenas: {concatenacion}") # Imprime: Concatenación de cadenas: 1234567890