# Manejo de caracteres especiales en cadenas de texto

# \n - Nueva línea
ejemplo_nueva_linea = "Hola,\nMundo!"
print(ejemplo_nueva_linea)

# \t - Tabulación
ejemplo_tabulacion = "Hola,\tMundo!"
print(ejemplo_tabulacion)

# \" - Comillas dobles
ejemplo_comillas_dobles = "Ella dijo: \"Hola, Mundo!\""
print(ejemplo_comillas_dobles)

# \' - Comillas simples
ejemplo_comillas_simples = 'Ella dijo: \'Hola, Mundo!\''
print(ejemplo_comillas_simples)
