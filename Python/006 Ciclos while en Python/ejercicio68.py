# sentencias de decision 

# Las sentencias de decision nos permiten controlar el flijo de ejecucion de un programa.
# Las estrictiras que podemos usar son: if, else y elif.
# - La sentencia if permite ejecutar un bloque de codigo si la condicion a evaliar es verdadera. Una condicion es una expresion que evalua a True o False, ej: edad >=18
# Sintaxis sentencia if:
# if condicion:
#     bloque de codigo
#     Ejecuta si la condicion es True
# Ejemplo Sentencia if:
edad = 30
if edad >=18:
    print('Eres mayor de edad.')