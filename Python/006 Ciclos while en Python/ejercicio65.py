# Ciclos en Python

# Los Ciclos en Python son estructuras de control que repiten una serie de instrucciones hasta que se cumple una condicion especifica.

# En python tenemos dos tipos de estructuras para ejecutar ciclos:
    # - While
    # - For

# Empezamos por el While. En este ciclo se repite una serie de instrucciones mientras la condicion a evaluar sea verdadera. 

# Sintaxis ciclo while:
# while condicion:
    # Bloque de codigo a ejecutar.

# Ejemplo ciclo while
# Imprimir de 1 a 3
contador = 1
while contador <= 3:
    print(contador)
    contador += 1
