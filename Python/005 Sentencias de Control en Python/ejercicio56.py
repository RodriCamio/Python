# Operador Ternario

# El operador ternadio en Python es una forma compacta de agregar una condicion, y el objetivo es asignar un valor a una variable dependiendo del valor de la condicion.

# Sintaxis Operador Ternario
# resultaro = valor_si_verdadero if condicion else valor_si_falso

# Ejemplo Operador Teernario
edad = 18
es_adulto = 'Si' if edad >= 18 else 'No'
print(es_adulto) # Salida: Si