# Operadores de Comparacion

# Los operadores condicionales se utilizan para comparar dos valores.

# El resultado siempre es un valor booleano 'True' o 'False', dependiendo de si la condicion se cumple o no.

# Operador de igualdad (==): Compara si dos calores son iguales:
# Sintaxis operador igualdad:
# a == b
# Ejemplo:
print(f'-'*30 + '\n=== Operador de igualdad ===\n')
print(f'5 == 5: {5==5}') # True
print(f'5 == 6: {5==6}') # False

# Operador de distinto (!=): Compara si dos valores son distintos:
# Sintaxis operador distinto:
# a != b
# Ejemplo:
print(f'\n' + '-'*30 + '\n=== Operador de distinto ===\n')
print(f'5 != 5 {5 != 5}') # False
print(f'5 != 6 {5 != 6}') # True

# Operador menor que (<): Compara si el valor de la izquierda es menor que el de la derecha. Si es menor devuelve True, sino False.
# Sintaxis operador menor que:
# a < b
# Ejemplo:
print(f'\n' + '-'*30 + '\n=== Operador menor que ===\n')
print(f'3 < 5 {3 < 5}') # True
print(f'5 < 3 {5 < 3}') # False

# Operador menor o igual que (<=): Compara si el valor de la izquierda es menor o igual que el de la derecha. Si es menor o igual devuelve True, sino False.
# Sintaxis operador menor o igual que:
# a <= b
# Ejemplo:
print(f'\n' + '-'*30 + '\n=== Operador menor o igual que ===\n')
print(f'3 <= 5 {3 <= 5}') # True
print(f'3 <= 3 {3 <= 3}') # True
print(f'5 <= 3 {5 <= 3}') # False

# Operador mayor que (<): Compara si el valor de la izquierda es mayor que el de la derecha. Si es mayor devuelve True, sino False.
# Sintaxis operador mayor que:
# a > b
# Ejemplo:
print(f'\n' + '-'*30 + '\n=== Operador mayor que ===\n')
print(f'5 > 3 {5 > 3}') # True
print(f'3 > 5 {3 > 5}') # False

# Operador mayor o igual que (<=): Compara si el valor de la izquierda es mayor o igual que el de la derecha. Si es mayor o igual devuelve True, sino False.
# Sintaxis operador mayor o igual que:
# a >= b
# Ejemplo:
print(f'\n' + '-'*30 + '\n=== Operador mayor o igual que ===\n')
print(f'5 >= 3 {5 >= 3}') # True
print(f'3 >= 3 {3 >= 3}') # True
print(f'3 >= 5 {3 >= 5}') # False
