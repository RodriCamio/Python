# Tipos de datos en Python
# "Ficha secreta de super héroe"

# Tipos de datos basicos en accion:

# string (cadena de texto)
nombre = "Clark Kent"
poder_secreto = "Super fuerza"

#integer (entero)
edad = 30
misiones_completadas = 150

# float (decimal)
nivel_energia = 99.5

# boolean (booleano)
tiene_capa = True
es_visible = False

# NoneType (tipo nulo)
arma_secreta = None

# Imprimimos la ficha del super héroe
print("=== Ficha del Super Héroe ===")
print("Nombre:", nombre)
print("Poder Secreto:", poder_secreto)
print("Edad:", edad)
print("Misiones Completadas:", misiones_completadas)
print("Nivel de Energía:", nivel_energia)
print("Tiene Capa:", tiene_capa)
print("Es Visible:", es_visible)
print("Arma Secreta:", arma_secreta)

print(type(nombre))
print(type(edad))
print(type(nivel_energia))
